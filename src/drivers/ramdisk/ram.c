void read_ram(Msg* m) {
    uint32_t i;
    uint8_t data;
    for (i = 0; i < m->len; i++) {
        if (m->offset + i > NR_MAX_FILE * NR_FILE_SIZE - 1) {
            break;
        }
        data = disk[m->offset + i];
        copy_from_kernel(
            fetch_pcb(m->req_pid),
            m->buf + i,
            &data,
            1);
    }
    m->ret = i;
    pid_t dest = m->src;
    m->src = RAM_DISK;
    send(dest, m);
}